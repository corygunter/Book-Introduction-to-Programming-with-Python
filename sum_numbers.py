# Sum Numbers

number1 = input('Enter the first number: ')
number2 = input('Enter the second number: ')
number3 = input('Enter the third number: ')
number4 = input('Enter the fourth number: ')


sum = float(number1) + float(number2)
total = float(number1) * float(number2) * float(number3) * float(number4)


print(f'The numbers {number1} and {number2} '
      f'add to {sum}')

print(f'The total of all numbers multiplied together is {total}!')



