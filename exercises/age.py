# Age
age = int(input("How old are you?\n"))
decade = 10

print(f'You are {age} years old.')
print(f'In {decade} years you will be {age + decade} years old.')
print(f'In {decade * 2} years you will be {age + decade * 2} years old.')
print(f'In {decade * 3} years you will be {age + decade * 3} years old.')
print(f'In {decade * 4} years you will be {age + decade * 4} years old.')